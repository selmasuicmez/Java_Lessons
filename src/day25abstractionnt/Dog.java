package day25abstractionnt;

public class Dog extends Animal {

	public static void main(String[] args) {

	}

	@Override
	public void eat() {
		System.out.println("Dogs eat");
		
	}

	@Override
	public void drink() {
		System.out.println("Dogs drink");
		
	}

	@Override
	public void breathe() {
		System.out.println("Dogs breathe");
		
	}

}
