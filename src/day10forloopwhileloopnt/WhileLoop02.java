package day10forloopwhileloopnt;

public class WhileLoop02 {

	public static void main(String[] args) {
		/*
		 Find the sum of first 10 counting numbers by using 
		 while loop.
		*/
		
		int i=1;
		int product=0;
		while(i<=4) {
			product = product * i;
			i++;
		}
		System.out.println(product);
		
		
		int j=1;

		int sum=0;

		while(j==1) {

				sum = sum + j;

				j++;

		}
		System.out.println(sum);

	}

}
