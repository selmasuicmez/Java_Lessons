package day09forloopnt;

public class ForLoop01 {

	public static void main(String[] args) {
		// If you need to do same action many times then using 
		// loop is the best solution
		
		//For example; I want to print "Hello World" 10 times on the console
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		System.out.println("Hello World");
		
		//By using for loop
		
		for(int i=1; i<=10; i++) {
			
			System.out.println("Hello World with for-loop");
			
		}

	}

}
